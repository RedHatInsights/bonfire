func TestExtend_HappyPath(t *testing.T) {
	res := sampleReservation() // duration=1h, state=active
	mc := noopClient()
	mc.listReservationsFn = func(_ context.Context, _ string) ([]map[string]any, error) {
		return []map[string]any{res}, nil
	}
	var patchedBody map[string]any
	mc.patchReservationFn = func(_ context.Context, _ string, patch map[string]any) (map[string]any, error) {
		patchedBody = patch
		return res, nil
	}

	result, err := Extend(context.Background(), mc, "ephemeral-abc123", "30m")
	mustNotErr(t, err)
	if result.Name != "test-reservation" {
		t.Errorf("Name = %q, want %q", result.Name, "test-reservation")
	}
	if result.NewDuration != "1h30m0s" {
		t.Errorf("NewDuration = %q, want %q", result.NewDuration, "1h30m0s")
	}
	spec, _ := patchedBody["spec"].(map[string]any)
	if spec["duration"] != "1h30m0s" {
		t.Errorf("patch spec.duration = %v, want '1h30m0s'", spec["duration"])
	}
}

