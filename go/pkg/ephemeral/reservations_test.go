func TestReserve_HappyPath(t *testing.T) {
	res := sampleReservation()
	calls := 0
	mc := noopClient()
	mc.getReservationFn = func(_ context.Context, _ string) (map[string]any, error) {
		calls++
		switch calls {
		case 1:
			return nil, nil // no existing reservation
		case 2:
			return res, nil // poll returns namespace
		default:
			return res, nil // final get
		}
	}
	mc.createReservationFn = func(_ context.Context, _ map[string]any) (map[string]any, error) {
		return res, nil
	}

	result, err := Reserve(context.Background(), mc, ReserveOptions{
		Name:      "test-reservation",
		Duration:  "1h",
		Requester: "test-user",
		Pool:      "default",
		Timeout:   10,
	})
	mustNotErr(t, err)
	if result.Name != "test-reservation" {
		t.Errorf("Name = %q, want %q", result.Name, "test-reservation")
	}
	if result.Namespace != "ephemeral-abc123" {
		t.Errorf("Namespace = %q, want %q", result.Namespace, "ephemeral-abc123")
	}
	if result.Requester != "test-user" {
		t.Errorf("Requester = %q, want %q", result.Requester, "test-user")
	}
	if result.Pool != "default" {
		t.Errorf("Pool = %q, want %q", result.Pool, "default")
	}
}

