func TestRelease_ByName(t *testing.T) {
	res := sampleReservation()
	mc := noopClient()
	mc.getReservationFn = func(_ context.Context, _ string) (map[string]any, error) {
		return res, nil
	}
	var patchedName string
	var patchedBody map[string]any
	mc.patchReservationFn = func(_ context.Context, name string, patch map[string]any) (map[string]any, error) {
		patchedName = name
		patchedBody = patch
		return res, nil
	}

	result, err := Release(context.Background(), mc, ReleaseOptions{Name: "test-reservation"})
	mustNotErr(t, err)
	if result.Name != "test-reservation" {
		t.Errorf("Name = %q, want %q", result.Name, "test-reservation")
	}
	if !result.Released {
		t.Error("Released should be true")
	}
	if patchedName != "test-reservation" {
		t.Errorf("patched name = %q, want %q", patchedName, "test-reservation")
	}
	spec, _ := patchedBody["spec"].(map[string]any)
	if spec["duration"] != "0s" {
		t.Errorf("patch spec.duration = %v, want '0s'", spec["duration"])
	}
}

